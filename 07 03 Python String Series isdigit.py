"""
'isalnum', 'isalpha', 'isascii', 'isnumeric', 'isdecimal', 'isdigit',
"""


num1 = "12485842484"

print('isalnum: ', num1.isalnum())
print('isnumeric: ', num1.isnumeric())
print('isdecimal: ', num1.isdecimal())
print('isdecimal: ', num1.isdecimal())
print('isdigit: ', num1.isdigit())

print('isascii: ', num1.isascii())

print('isalpha: ', num1.isalpha())